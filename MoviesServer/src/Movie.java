
public class Movie {
    
    String title, director, genre, duration, description;

    public Movie(String title, String director, String genre, String duration, String description) {
        this.title = title;
        this.director = director;
        this.genre = genre;
        this.duration = duration;
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public String getDirector() {
        return director;
    }

    public String getGenre() {
        return genre;
    }

    public String getDuration() {
        return duration;
    }

    public String getDescription() {
        return description;
    }

    
    
    
    
}
